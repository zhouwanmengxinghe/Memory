                                    }
                                }
                            },
                            leadingIcon = {
                                Icon(Icons.Default.Share, contentDescription = null)
                            },
                            enabled = !isExporting && events.isNotEmpty()
                        )
                        DropdownMenuItem(
                            text = { Text("ÂØºÂÖ•Â§á‰ªΩ") },
                            onClick = {
                                showMenu = false
