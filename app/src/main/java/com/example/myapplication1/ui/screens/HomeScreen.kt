                                            viewModel.stopAudio()
                                        } else {
                                            viewModel.playAudio(audioPath)
                                        }
                                    }
                                }
                            },
                            isPlayingAudio = uiState.isPlayingAudio
                        )
                    }
                }
            }
        }
        
