                                    }
                                }
                            },
                            leadingIcon = {
                                Icon(Icons.Default.Download, contentDescription = null)
                            },
                            enabled = !isExporting && events.isNotEmpty()
                        )
                        DropdownMenuItem(
                            text = { Text("ÂàÜ‰∫´Â§á‰ªΩ") },
                            onClick = {
                                showMenu = false
                                scope.launch {
                                    isExporting = true
                                    try {
                                        val backupFile = ExportImportUtils.exportMemories(context, events, emptyList())
                                        backupFile?.let { file ->
